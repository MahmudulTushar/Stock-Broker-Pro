using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BusinessAccessLayer.BO
{
    public class CashbackCriteriaBO
    {

        private int _iD;
        private string _planName;
        private string _planDescription;
        private DateTime _planDate;
        private float _minTradeAmount;
        private float _rate;
        private DateTime _effectiveDate;
        public CashbackCriteriaBO()
        {
            _planName = "";
            _minTradeAmount =0;
            _rate = 0;
            _planDescription = "";
        }

        public int ID
        {
            get { return _iD; }
            set { _iD = value; }
        }

        public string PlanName
        {
            get { return _planName; }
            set { _planName = value; }
        }

        public float MinTradeAmount
        {
            get { return _minTradeAmount; }
            set { _minTradeAmount = value; }
        }

        public float Rate
        {
            get { return _rate; }
            set { _rate = value; }
        }

        public DateTime EffectiveDate
        {
            get { return _effectiveDate; }
            set { _effectiveDate = value; }
        }

        public DateTime PlanDate
        {
            get { return _planDate; }
            set { _planDate = value; }
        }

        public string PlanDescription
        {
            get { return _planDescription; }
            set { _planDescription = value; }
        }
    }
}
