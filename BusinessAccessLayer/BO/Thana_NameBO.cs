using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BusinessAccessLayer.BO
{
   public class Thana_NameBO
    {
        public Thana_NameBO()
        {
            _thana_Code = "";
            _thana_Name = string.Empty;
            _thana_Description = string.Empty;
        }

        private string _thana_Code;
        private string _thana_Name;
        private string _thana_Description;

        public string Thana_Code
        {
            get { return _thana_Code; }
            set { _thana_Code = value; }
        }

        public string ThanaName
        {
            get { return _thana_Name; }
            set { _thana_Name = value; }
        }

        public string Thana_Description
        {
            get { return _thana_Description; }
            set { _thana_Description = value; }
        }
    }
}
