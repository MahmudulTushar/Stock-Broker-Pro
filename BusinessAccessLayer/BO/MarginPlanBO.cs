using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BusinessAccessLayer.BO
{
    public class MarginPlanBO
    {
        private string _custCode;
        private string _planName;
        private int _effectiveCount;
        private float _chargeRate;
        private double _CommissionRate;
        private float _freeAmount;
        private float _marginRatio;
        private string _remarks;
        private DateTime _eDate;

        public MarginPlanBO()
        {
            _planName="";
            _effectiveCount=0;
            _chargeRate=0;
            _CommissionRate = 0.00;
            _freeAmount=0;
            _marginRatio=0;
            _remarks="";
        }

        public string CustCode
        {
            get { return _custCode; }
            set { _custCode = value; }
        }

        public string PlanName
        {
            get { return _planName; }
            set { _planName = value; }
        }

        public int EffectiveCount
        {
            get { return _effectiveCount; }
            set { _effectiveCount = value; }
        }

        public float ChargeRate
        {
            get { return _chargeRate; }
            set { _chargeRate = value; }
        }
        public double? CommissionRate { get; set; }
       
        public float FreeAmount
        {
            get { return _freeAmount; }
            set { _freeAmount = value; }
        }

        public float MarginRatio
        {
            get { return _marginRatio; }
            set { _marginRatio = value; }
        }

        public string Remarks
        {
            get { return _remarks; }
            set { _remarks = value; }
        }

        public DateTime EDate
        {
            get { return _eDate; }
            set { _eDate = value; }
        }
    }
}
