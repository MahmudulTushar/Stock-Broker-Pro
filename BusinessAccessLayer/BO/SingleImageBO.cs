using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BusinessAccessLayer.BO
{
    public class SingleImageBO
    {
        private string _custCode;
        private string _imageSelectionMode;
        private byte[] _bArrayImgByte;
        public SingleImageBO()
        {
            _imageSelectionMode = "";
            _bArrayImgByte = null;
        }

        public string CustCode
        {
            get { return _custCode; }
            set { _custCode = value; }
        }

        public string ImageSelectionMode
        {
            get { return _imageSelectionMode; }
            set { _imageSelectionMode = value; }
        }

        public byte[] ImgByte
        {
            get { return _bArrayImgByte; }
            set { _bArrayImgByte = value; }
        }
    }
}
