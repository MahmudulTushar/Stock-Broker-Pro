using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace BusinessAccessLayer.BO
{
    public class CheckRequisitionBO
    {
        private int _slNo;
        private string _custCode;
        private double _amount;
        private int _branchId;
        private DateTime _requisitionDate;
        private string _remarks;
        private int _onlineOrderNo;
        private DateTime _onlineEntry_Date;

        private string _Deposit_Withdraw;
        private string _Payment_Media;

        

        public CheckRequisitionBO()
        {
            _custCode = "";
            _amount = 0;
            _branchId = 0;
            _remarks = "";
            _requisitionDate = DateTime.Today;
        }


        public int SlNo
        {
            get { return _slNo; }
            set { _slNo = value; }
        }

        public string CustCode
        {
            get { return _custCode; }
            set { _custCode = value; }
        }

        public double Amount
        {
            get { return _amount; }
            set { _amount = value; }
        }

        public int BranchId
        {
            get { return _branchId; }
            set { _branchId = value; }
        }

        public DateTime RequisitionDate
        {
            get { return _requisitionDate; }
            set { _requisitionDate = value; }
        }

        public string Remarks
        {
            get { return _remarks; }
            set { _remarks = value; }
        }
      

        public DateTime OnlineEntry_Date
        {
            get { return _onlineEntry_Date; }
            set { _onlineEntry_Date = value; }
        }

        public int OnlineOrderNo
        {
            get { return _onlineOrderNo; }
            set { _onlineOrderNo = value; }
        }

        public string Payment_Media
        {
            get { return _Payment_Media; }
            set { _Payment_Media = value; }
        }

        public string Deposit_Withdraw
        {
            get { return _Deposit_Withdraw; }
            set { _Deposit_Withdraw = value; }
        }
        private string _Channel;

        public string Channel
        {
            get { return _Channel; }
            set { _Channel = value; }
        }

    }
}
