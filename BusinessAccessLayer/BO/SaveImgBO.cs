using System.Linq;

namespace BusinessAccessLayer.BO
{
    public class SaveImgBO
    {
        private string _customeCode;
        private byte[] _bArrayImage;
        private byte[] _bSecArrayImage;
        private string _imagePurpose;
        private int _formSaveid;

        public SaveImgBO()
        {
            
            _bArrayImage = null;
            _bSecArrayImage = null;
            _imagePurpose = null;
        }

        public string CustomerCode
        {
            get { return _customeCode; }
            set{ _customeCode = value; }
        }

        public byte[] ImageArrayByte
        {
            get
            {
                return _bArrayImage;
            }
            set
            {
                _bArrayImage = value;
            }
        }

        public byte[] SecImageArrayByte
        {
            get
            {
                return _bSecArrayImage;
            }
            set
            {
                _bSecArrayImage = value;
            }
        }

        public string ImagePurpose
        {

            get
            {
                return _imagePurpose;
            }
            set
            {
                _imagePurpose = value;
            }
        
        }

        public int FormSaveID
        {

            get
            {
                return _formSaveid;
            }
            set
            {
                _formSaveid = value;
            }
        
        }
       
    }
}
